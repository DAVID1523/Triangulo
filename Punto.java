
public class Punto
{
    private double x;
    private double y;
    
    
    // CONSTRUCTORES
    public Punto(){
       
    }
    
    public Punto(double x, double y){
    this.x=x;
    this.y=y;
    }
        
    //GETTER AND SETTER
    
    public double getX(){
    return x;
    }
    
    public double getY(){
    return y;
    }
    
    public void setX(double x){
    this.x=x;
    }
    
    public void setY(double y){
    this.y=y;
    }
    
    
}


